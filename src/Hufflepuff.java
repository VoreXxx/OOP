public class Hufflepuff {
    int hardworking;
    int loyal;
    int honest;

    public Hufflepuff(int hardworking, int loyal, int honest) {
        this.hardworking = hardworking;
        this.loyal = loyal;
        this.honest = honest;
    }
    void description() {
        System.out.println("Hardworking: " + hardworking + " Loyal: " + loyal + " Honest: " + honest);
    }
}
